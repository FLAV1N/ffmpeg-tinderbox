#!/bin/bash

LIBRIST_REPO="https://github.com/nanake/librist.git"
LIBRIST_COMMIT="a1a4b278c5e71ec7145d6865e9df67ed4df1e6af"

ffbuild_enabled() {
    return 0
}

ffbuild_dockerbuild() {
    git-mini-clone "$LIBRIST_REPO" "$LIBRIST_COMMIT" librist
    cd librist

    mkdir build && cd build

    local myconf=(
        --prefix="$FFBUILD_PREFIX"
        --buildtype=release
        --default-library=static
        -Dhave_mingw_pthreads=true
        -D{built_tools,test,use_mbedtls}"=false"
        # Workaround/fixes for cJSON symbol collision
        -Ddisable_json=true
    )

    if [[ $TARGET == win* ]]; then
        myconf+=(
            --cross-file=/cross.meson
        )
    else
        echo "Unknown target"
        return -1
    fi

    meson "${myconf[@]}" ..
    ninja -j"$(nproc)"
    ninja install
}

ffbuild_configure() {
    echo --enable-librist
}

ffbuild_unconfigure() {
    echo --disable-librist
}
